package LinkedList;

public class Node {
    private int data;
    private Node nodeNext;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public Node getNodeNext() {
        return nodeNext;
    }

    public void setNodeNext(Node nodeNext) {
        this.nodeNext = nodeNext;
    }
}
